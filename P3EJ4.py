#Pide al usuario que introduzca 3 calificaciones, y calcule la media de estas.
Primera_calificación=float(input("Introduzca su primera calificación"))
Segunda_calificación=float(input("Introduzca su segunda calificación"))
Tercera_calificación=float(input("Introduzca su tercera calificación"))
media=((Primera_calificación+Segunda_calificación+Tercera_calificación)/3)
print("Su media es", media)